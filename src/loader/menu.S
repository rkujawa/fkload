init_video:
	lea			custombase,a0
	move.w		#$1200,bplcon0(a0)	; set lo-res mode w/o interlace
	move.w		#0,bplcon1(a0)		; set horizontal scroll value to 0
	move.w		#0,bpl1mod(a0)		; set modulo to 0
	move.w		#0,bpl2mod(a0)
	move.w		#$0038,ddfstrt(a0)	; set data-fetch start
	move.w		#$00D0,ddfstop(a0)	; set data-fetch stop

	move.w		#$2C81,diwstrt(a0)	; set window start
	move.w		#$2CC1,diwstop(a0)

	move.w		#$0000,color00(a0)	; black into color register 0
	move.w		#$0fff,color01(a0)	; sky blue into color register 1

    lea         copperlist_scroll,a1
	lea			planebuffer,a2
	move.l		a2,d2

	; set pointers to bitplane at the beginning of copperlist
	move.w		#bpl1pth,(a1)+
	swap.w		d2
	move.w		d2,(a1)+

	move.w		#bpl1ptl,(a1)+
	move.w		a2,(a1)+
    
	
	move.l      #32,d2                  ; number of lines
    move.l      #$66,d0                 ; first line
    lea         sin_table,a2
;breakpoint
;	cmp.l		#31,d2
;	bne			breakpoint


red_bar_loop
	move.b		(a2),d1		; read sine value
	lsl.l		#4,d1		; scale the sine so that the higher nibble of sine value is at the position of red component
	and.l		#$00000F00,d1
	swap		d1			; swap words so that red will be in color00
	add.l		#8,a2
	bsr			addline
	add.l		#1,d0
	dbra		d2,red_bar_loop
	
	move.l		#31,d2
	move.l		#$96,d0
	lea			sin_table,a2

blue_bar_loop
    ; get color
    move.b      (a2),d1     ; read sine value
    lsr.l       #4,d1       ; scale sine value from 0-255 to 0-15
	and.l		#$0000000F,d1
	add.l       #8,a2       ; move to next sine table index
    bsr         addline     ; add WAIT instruction to copperlist (and change the color - this should be in a different routine)
    add.l       #1,d0       ; increment line
    dbra        d2,blue_bar_loop

	move.w		#color00,(a1)+
	move.w		#$0000,(a1)+  
	move.w		#color01,(a1)+
	move.w		#$0000,(a1)+  
	
    move.l      #$FFFFFFFE,(a1)         ; end of copperlist


    lea         copperlist_scroll,a1
    move.l		a1,cop1lch(a0)	; write to Copper location register
	move.w		d0,copjmp1(a0)

    move.w      #$8380,dmacon(a0)
    move.w      #$C030,intena(a0)

;	lea			planebuffer,a1
vblank:
;	move.l		a1,bpl1pth(a0)

waiting:
	move.w      intreqr(a0),d0          ; read interrupt requests
    andi.w      #$0020,d0               ; mask off all but vertical blank
    beq         waiting
    
	move.w      #$0020,intreq(a0)       ; reset vertical interrupt
   	jmp			vblank 

.1:	jmp		.1


; a1 - address (will be changed), d0 - line, d1 - color (color01 in upper word)
addline
	move.l		d0,d3
    move.l		d2,d4
    ; put WAIT instruction
    lsl.l		#8,d0			; move the line number to the highest byte
    swap		d0 	
	andi.l		#$FF000000,d0	; mask all but the highest byte
    
    move.l		#$0001FF00,d2   ; WAIT instruction template
    or.l		d0,d2			; set the color value
    move.l		d2,(a1)+		; append the instruction to the list
    
    move.w		#color00,(a1)+  ; first color register
    move.w		d1,(a1)+        ; set color value
	swap		d1
    move.w		#color01,(a1)+  ; second color register
    move.w		d1,(a1)+

    move.l      d3,d0
    move.l      d4,d2
    rts



