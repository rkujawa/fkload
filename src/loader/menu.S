menu:

	; setup the planebuffer by writing a simple pattern to it
	lea			planebuffer,a2		; load planebuffer address
	move.l		#$9FF,d0				
	move.l		#$FF00FF00,d1		; set pattern in d1

.loop
	move.l		d1,(a2)+			; fill the planebuffer
	dbra		d0,.loop


; start DMA

